package com.danny.elder.bean;

public class SocialWorker {
	String s_id;
	String s_password;
	String s_name;
	String s_tel_no;
	String s_position;
	String w_code;
	
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getS_password() {
		return s_password;
	}
	public void setS_password(String s_password) {
		this.s_password = s_password;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_tel_no() {
		return s_tel_no;
	}
	public void setS_tel_no(String s_tel_no) {
		this.s_tel_no = s_tel_no;
	}
	public String getS_position() {
		return s_position;
	}
	public void setS_position(String s_position) {
		this.s_position = s_position;
	}
	public String getW_code() {
		return w_code;
	}
	public void setW_code(String w_code) {
		this.w_code = w_code;
	}
}
