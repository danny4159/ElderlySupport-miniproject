package com.danny.elder.bean;

public class HistorySupport {
	String h_code;
	int h_support_fund;
	String h_support_date;
	String e_id;
	String ew_code;
	
	public String getH_code() {
		return h_code;
	}
	public void setH_code(String h_code) {
		this.h_code = h_code;
	}
	public int getH_support_fund() {
		return h_support_fund;
	}
	public void setH_support_fund(int h_support_fund) {
		this.h_support_fund = h_support_fund;
	}
	public String getH_support_date() {
		return h_support_date;
	}
	public void setH_support_date(String h_support_date) {
		this.h_support_date = h_support_date;
	}
	public String getE_id() {
		return e_id;
	}
	public void setE_id(String e_id) {
		this.e_id = e_id;
	}
	public String getEw_code() {
		return ew_code;
	}
	public void setEw_code(String ew_code) {
		this.ew_code = ew_code;
	}
}
